package com.team5.justdoeat.store.vo;

import com.team5.justdoeat.store.entity.DeliveryFeeInfoEntity;

import lombok.Data;

@Data
public class DeliveryFeeInfoVO {
  private Long seq;
  private Long storeInfo;
  private Integer minFee;
  private Integer maxFee;
  private Integer deliveryFee;
  
  private DeliveryFeeInfoVO(DeliveryFeeInfoEntity entity){
    this.seq = entity.getDfiSeq();
    this.storeInfo = entity.getDfiSiSeq();
    this.minFee = entity.getDfiMinFee();
    this.maxFee = entity.getDfiMaxFee();
    this.deliveryFee = entity.getDfiDeliveryFee();
  }
}
